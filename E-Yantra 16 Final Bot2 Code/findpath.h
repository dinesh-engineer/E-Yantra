void findPath (int src, int dest)
{
  
  visited[src] = true;
  path[indx] = src;
  indx++;

  if (src == dest)
    {
      print_elements ();
    }
  else
    {
      int i;
      for (i = 0; i < N; i++)
	{
	  if (visited[i] == false && graph[src][i])
	    findPath (i, dest);
	}
    }

  visited[src] = false;
  indx--;
}

void print_elements ()
{
  int i;
  if(indx < final_path_len){
  	for(i = 0; i < final_path_len;i++){
  		if(i < indx){
  			final_path[i] = path[i];
		  }	
		else
			final_path[i] = 0;
	}
	final_path_len = indx;
  }
  
}

